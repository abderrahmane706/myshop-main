export function getProduct(idOrSlug) {
  return PRODUCTS.find(p => p.id === idOrSlug || p.slug === idOrSlug);
}
export function getProductsByCategory(slug) {
  if (!slug || slug === 'all') return PRODUCTS;
  if (slug === 'new-collection') return PRODUCTS.filter(p => p.tags.includes('new-collection'));
  if (slug === 'sale') return PRODUCTS.filter(p => p.tags.includes('sale') || p.compare_at);
  return PRODUCTS.filter(p => p.category === slug);
}
export function getBestsellers() { return PRODUCTS.filter(p => p.bestseller); }
export function getFeatured() { return PRODUCTS.filter(p => p.featured); }
export function getNewArrivals() { return PRODUCTS.filter(p => p.tags.includes('new-collection')); }
